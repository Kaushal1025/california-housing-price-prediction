#House price prediction model
import numpy as np #useful for arrays
import pandas as pd #the data frame
import matplotlib.pyplot as plt # useful for plots and graphing
import seaborn as sns # another library for plots
import sklearn.datasets #datasets
from sklearn.datasets import fetch_california_housing #ML algorithms
from sklearn.model_selection import train_test_split
from xgboost import XGBRegressor
from sklearn import metrics
import joblib

# Load dataset
house_price_dataset = sklearn.datasets.fetch_california_housing()

# Create dataframe
house_price_dataframe = pd.DataFrame(house_price_dataset.data, columns = house_price_dataset.feature_names)

# Display first few rows
house_price_dataframe.head()

# Add price column
house_price_dataframe['price']= house_price_dataset.target
house_price_dataframe.head()

# Check shape
house_price_dataframe.shape

# Check for missing values
house_price_dataframe.isnull().sum()

# Statistical measures
house_price_dataframe.describe()

# Correlation
correlation = house_price_dataframe.corr()

# Heatmap
plt.figure(figsize=(10,10))
sns.heatmap(correlation, cbar=True, square=True, fmt='.1f', annot=True, annot_kws={'size':8}, cmap='Blues')

# Split data and target
X = house_price_dataframe.drop(['price'], axis=1)
Y = house_price_dataframe['price']
print(X)
print(Y)

# Split into training and test data
X_train, X_test, Y_train, Y_test = train_test_split(X, Y, test_size=0.2, random_state=2)
print(X.shape, X_train.shape, X_test.shape)

# Train model
model = XGBRegressor()
model.fit(X_train, Y_train)

# Training predictions
training_data_prediction = model.predict(X_train)

score_1 = metrics.r2_score(Y_train, training_data_prediction)
score_2 = metrics.mean_absolute_error(Y_train, training_data_prediction)

print("R squared error : ", score_1)
print("Mean absolute error : ", score_2)

# Save model
joblib.dump(model, "california_model.pkl")
print("Model saved as california_model.pkl")

